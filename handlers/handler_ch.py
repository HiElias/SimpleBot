from aiogram import types
from keyboards import kb_urls_ch
import texts
from async_table import table_add
from datetime import datetime


async def call29(callback: types.CallbackQuery):
    await callback.message.delete()
    await callback.message.answer(texts.text29, reply_markup=kb_urls_ch)
    await callback.answer()
    await table_add((callback.from_user, call29.__name__, datetime.now()))


